package swinglibraryproject;

/**
 *
 * @author Odain
 */
public class Book 
{
    private static String IDNumber;
    private static String Title;
    private static String Year;
    private static String Publisher;
    private static String Edition;
    private static String Genre;
    private static String Author;
    private static String Status;
    private static String Condition;

    public static String getIDNumber() {
        return IDNumber;
    }

    public static void setIDNumber(String aIDNumber) {
        IDNumber = aIDNumber;
    }

    public static String getTitle() {
        return Title;
    }

    public static void setTitle(String aTitle) {
        Title = aTitle;
    }

    public static String getYear() {
        return Year;
    }

    public static void setYear(String aYear) {
        Year = aYear;
    }

    public static String getPublisher() {
        return Publisher;
    }

    public static void setPublisher(String aPublisher) {
        Publisher = aPublisher;
    }

    public static String getEdition() {
        return Edition;
    }

    public static void setEdition(String aEdition) {
        Edition = aEdition;
    }

    public static String getGenre() {
        return Genre;
    }

    public static void setGenre(String aGenre) {
        Genre = aGenre;
    }

    public static String getAuthor() {
        return Author;
    }

    public static void setAuthor(String aAuthor) {
        Author = aAuthor;
    }

    public static String getStatus() {
        return Status;
    }

    public static void setStatus(String aStatus) {
        Status = aStatus;
    }

    public static String getCondition() {
        return Condition;
    }

    public static void setCondition(String aCondition) {
        Condition = aCondition;
    }
    
    
}//END OF BOOK CLASS
